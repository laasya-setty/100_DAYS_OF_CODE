package Competetive_programming;
import java.util.*;
public class HexawareThree {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        String s=sc.nextLine();
        s=s.replaceAll("[^\\w\\s]","");
        String[] str=s.split(" ");
        System.out.println(Arrays.toString(str));
        sc.close();
     sc.close();
    }
}
